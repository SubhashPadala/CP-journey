int n, m;
vector<int> adj[MAX_N], dist;
int parent[MAX_N];

void bfs(int s) {
    dist.assign(n + 1, -1);
    queue<int> q;
    dist[s] = 0; q.push(s);
    while (q.size()) {
        int u = q.front(); q.pop();
        for (int v : adj[u]) {
            if (dist[v] == -1) {
            	parent[v] = u;
                dist[v] = dist[u] + 1;
                q.push(v);
            }
        }
    }
}

void solve() {
    cin >> n >> m;
    for (int i = 0; i < m; i++) {
        int u, v; cin >> u >> v;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }

    bfs(1);
    if (dist[n] == -1){
    	cout << "IMPOSSIBLE";
    	return;
    }

    cout << dist[n]+1 << "\n";

    int i = n;
    stack<int> stk;
    while(i != 0){
    	stk.push(i);
    	i = parent[i];
    }

    while(!stk.empty()){
    	cout << stk.top() << " ";
    	stk.pop();
    }

}